package transferobject;

import java.util.Date;


public class VendaTO {
	private int id;
	private Date data;
	private int login;
	

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public Date getData() {
		return data;
	}

	public void setData(Date data) 
		{
		this.data = data;
	}

	public int getLogin() {
		return login;
	}

	public void setLogin(int login) {
		this.login = login;
	}
	
	

}
